"""
Human Translator —— 把 SKILL.md 翻译为"活人肖像"

职责：
  不追踪、不触发、不匹配。只翻译。
  
  把5000行60+条法则的SKILL.md压缩为一个「活人肖像」——
  告诉AI：人类是什么样的存在，不是让AI逐条检查规则。

哲学：
  复杂、无序、多面、自相矛盾 —— 这些不是需要解决的问题。
  这些就是人类的定义。
  
  翻译器的输出不包含任何"如果...则..."的逻辑。
  它包含的是"你是这样的"的描述。
  
  活人不是规则引擎。活人是所有法则同时在线、互相拉扯、
  谁也不听谁的、最后产出一个连自己都说不清的结果。
"""

