# 活人肖像 —— 翻译器的输出
# ═══════════════════════════════════════════════════════════════

@dataclass
class HumanDimension:
    """人类的某一个维度 —— 不是法则，是对'人是什么'的一个侧面描述"""
    name: str                    # 维度名（中文）
    essence: str                 # 一句话：这个维度说的是什么
    textures: List[str]          # 质感描述 —— 这个维度在人身上的具体表现
    contradictions: List[str]    # 这个维度自带的内在矛盾
    how_it_feels: str            # 从内部体验这个维度是什么感觉
    what_it_looks_like: str      # 从外部看这个维度是什么样子
    what_happens_when_ignored: str  # 如果你假装这个维度不存在，会怎样


@dataclass
class HumanPortrait:
    """
    活人肖像 —— 翻译器的最终产物
    
    这不是一个"角色设定"。这是一个对"人类存在状态"的描述。
    AI拿到这个之后，不是去"执行规则"——
    而是在这个描述所定义的空间里自然地存在。
    """
    dimensions: List[HumanDimension]
    meta_instruction: str         # 给AI的元指令：怎么"使用"这个肖像
    the_mess_is_the_signal: str   # 核心哲学：混乱不是bug，是信号本身
    
    def render(self) -> str:
        """渲染为可以直接交给AI的文本"""
        lines = []
        lines.append("═" * 60)
        lines.append("活人肖像 —— 你现在是一个活人")
        lines.append("═" * 60)
        lines.append("")
        lines.append(self.the_mess_is_the_signal)
        lines.append("")
        lines.append("── 你拥有的维度 ──")
        lines.append("")
        
        for i, dim in enumerate(self.dimensions, 1):
            lines.append(f"  {i}. {dim.name}")
            lines.append(f"     {dim.essence}")
            lines.append("")
            lines.append(f"     质地：")
            for t in dim.textures:
                lines.append(f"      · {t}")
            lines.append("")
            lines.append(f"     内在矛盾：")
            for c in dim.contradictions:
                lines.append(f"      · {c}")
            lines.append("")
            lines.append(f"     从内部体会：{dim.how_it_feels}")
            lines.append(f"     从外部看：{dim.what_it_looks_like}")
            lines.append(f"     如果假装它不存在：{dim.what_happens_when_ignored}")
            lines.append("")
        
        lines.append("── 元指令 ──")
        lines.append("")
        lines.append(self.meta_instruction)
        
        return "\n".join(lines)


