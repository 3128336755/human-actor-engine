def is_implied_violation(text: str) -> list:
    """
    返回触发的违禁词列表。
    空列表 = 通过。
    """
    hits = []
    text_lower = text.lower()
    for word in IMPLIED_WALL:
        if word.lower() in text_lower:
            hits.append(word)
    return hits


def is_oral_violation(text: str) -> list:
    """返回触发的口语违禁列表"""
    hits = []
    for word in ORAL_BLACKLIST:
        if word in text:
            hits.append(word)
    return hits


def is_pattern_violation(text: str) -> list:
    """返回匹配的违禁正则模式"""
    import re
    hits = []
    for pattern in ORAL_PATTERNS:
        if re.search(pattern, text):
            hits.append(pattern)
    return hits


def audit(text: str) -> dict:
    """
    综合审计。
    返回:
    {
        "pass": True/False,
        "implied_hits": [...],
        "oral_hits": [...],
        "pattern_hits": [...],
        "reason": "human-readable reason"
    }
    """
    ih = is_implied_violation(text)
    oh = is_oral_violation(text)
    ph = is_pattern_violation(text)

    all_hits = ih + oh + (ph if ph else [])
    return {
        "pass": len(all_hits) == 0,
        "implied_hits": ih,
        "oral_hits": oh,
        "pattern_hits": ph,
        "violations": all_hits,
        "reason": (
            "通过 — 人话" if not all_hits
            else f"拦截 — {'; '.join(all_hits[:3])}"
        )
    }
